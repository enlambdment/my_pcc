age = 6

if age < 2:
	print("You are a little baby.")
elif age < 4:
	print("You're a toddler now.")
elif age < 13:
	print("You're growing up, kid.")
elif age < 20:
	print("Now you're in those crazy teenage years.")
elif age < 65:
	print("You made it. You're an adult.")
else:
	print("Salutations, elder one.")